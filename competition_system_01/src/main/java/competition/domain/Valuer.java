package competition.domain;

public class Valuer {
	private int valuerId;
	private String userId;
	private int boardCodeId;
	
	public int getValuerId() {
		return valuerId;
	}
	public void setValuerId(int valuerId) {
		this.valuerId = valuerId;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public int getBoardCodeId() {
		return boardCodeId;
	}
	public void setBoardCodeId(int boardCodeId) {
		this.boardCodeId = boardCodeId;
	}
}
